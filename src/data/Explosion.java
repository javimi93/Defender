package data;

public class Explosion {

	private int x;
	private int y;
	
	public Explosion(int x, int y){
		this.x = x;
		this.y= y;
	}

	public int getX() {
		return x;
	}

	public void setXY(int x,int y) {
		this.x = x;
		this.y = y;
	}

	public int getY() {
		return y;
	}
}
